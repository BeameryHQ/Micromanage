# link_node_modules
# Remove all node_modules from all the repos and run zelda to link and download all

link_node_modules() {

    printf "${RED}Please note that this will remove all node_modules from all repos\n${NC}"
    read -p "Do you want to clean node_modules before linking? [Y/N] " -n 1;
    if [[ $REPLY =~ ^[yY]$ ]]; then
        execute -n $@ "echo ' ---> removing node_modules' && rm -rf node_modules && zelda ../"
    else
        execute -n $@ "zelda ../"
    fi
}
